interface LocalizableOption {
  value: string;
  label: string;
  labelI18n?: { en?: string; ms?: string; zh?: string; ta?: string };
}
interface LocalizableQuestion {
  label: string;
  labelI18n?: { en?: string; ms?: string; zh?: string; ta?: string };
  options?: LocalizableOption[];
  [k: string]: unknown;
}

/** Attach labelI18n (from QUESTION_I18N) to each question and option that has a
 *  translation entry, leaving everything else untouched. Pure - returns new objects. */
export function localizeQuestions<T extends LocalizableQuestion>(questions: T[]): T[] {
  const lookup = (label: string) => {
    const t = QUESTION_I18N[label];
    return t ? { en: label, ms: t.ms, zh: t.zh, ta: t.ta } : undefined;
  };
  return questions.map((q) => {
    const qI18n = lookup(q.label);
    const options = q.options?.map((o) => {
      const oI18n = lookup(o.label);
      return oI18n ? { ...o, labelI18n: oI18n } : o;
    });
    return {
      ...q,
      ...(qI18n ? { labelI18n: qI18n } : {}),
      ...(options ? { options } : {}),
    };
  });
}
